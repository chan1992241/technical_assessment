package online.chanjinyee.technical_assessment;

public class Earth_Time_Body {
    private int earthYear;
    private int earthMonth;
    private int earthDay;
    private int earthHour;
    private int earthMinute;
    private int earthSecond;

    public int getEarthYear() {
        return earthYear;
    }

    public void setEarthYear(int earthYear) {
        this.earthYear = earthYear;
    }

    public int getEarthMonth() {
        return earthMonth;
    }

    public void setEarthMonth(int earthMonth) {
        this.earthMonth = earthMonth;
    }

    public int getEarthDay() {
        return earthDay;
    }

    public void setEarthDay(int earthDay) {
        this.earthDay = earthDay;
    }

    public int getEarthHour() {
        return earthHour;
    }

    public void setEarthHour(int earthHour) {
        this.earthHour = earthHour;
    }

    public int getEarthMinute() {
        return earthMinute;
    }

    public void setEarthMinute(int earthMinute) {
        this.earthMinute = earthMinute;
    }

    public int getEarthSecond() {
        return earthSecond;
    }

    public void setEarthSecond(int earthSecond) {
        this.earthSecond = earthSecond;
    }
}